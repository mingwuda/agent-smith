let currentLanguage = localStorage.getItem('desktop-agent-language') || ((navigator.language || '').toLowerCase().startsWith('zh') ? 'zh-CN' : 'en');
if (!I18N[currentLanguage]) currentLanguage = 'zh-CN';

function t(key, params = {}) {
  const template = (I18N[currentLanguage] && I18N[currentLanguage][key]) || I18N['zh-CN'][key] || key;
  return template.replace(/\{(\w+)\}/g, (_, name) => params[name] ?? '');
}

function applyI18n() {
  document.documentElement.lang = currentLanguage;
  document.title = t('appTitle');
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.setAttribute('placeholder', t(el.dataset.i18nPlaceholder));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.setAttribute('title', t(el.dataset.i18nTitle));
  });
  updateLangLabel();
  setSendButtonRunning(isLoading);
}

function switchLanguage() {
  closeUserMenu();
  currentLanguage = currentLanguage === 'zh-CN' ? 'en' : 'zh-CN';
  localStorage.setItem('desktop-agent-language', currentLanguage);
  applyI18n();
  updateLangLabel();
  renderSessionList(sessionsCache, currentSessionId);
  refreshSkills();
  checkHealth();
}
function updateLangLabel() {
  const el = document.getElementById('user-menu-lang');
  if (el) el.textContent = currentLanguage === 'zh-CN' ? 'English' : '中文';
}
